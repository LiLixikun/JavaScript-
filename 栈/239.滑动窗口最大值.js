// @lc code=start
/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number[]}
 */
var maxSlidingWindow = function (nums, k) {
  let queue = []
  let res = []
  for (let i = 0; i < nums.length; i++) {
    const num = nums[i]
    while (nums[queue[queue.length - 1]] <= num) {
      queue.pop()
    }
    queue.push(i)
    // 判断当前最大值（即队首元素）是否在窗口中，若不在便将其出队
    if (queue[0] <= i - k) {
      queue.shift()
    }
    // 从 第 K 个开始算最大值
    if (i >= k - 1) {
      res.push(nums[queue[0]])
    }
  }
  return res
};
maxSlidingWindow([1, 3, -1, -3, 5, 3, 6, 7], 3)
